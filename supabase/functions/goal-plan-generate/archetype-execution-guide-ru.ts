/**
 * Как превращать сильные стороны архетипа в исполнимые шаги к КОНКРЕТНОЙ цели.
 * Модель обязана следовать формату «чеклист» — человек только выполняет, не додумывает.
 */
export const ARCHETYPE_EXECUTION_GUIDE_RU = `
ЧАСТЬ 5: ИСПОЛНЯЕМЫЕ ШАГИ ПО АРХЕТИПАМ (магия = конкретика)

КОНТРАКТ КАЖДОГО ШАГА (phases.steps, first_week, archetype_tips):
Каждый пункт ОБЯЗАН содержать явно:
1) ДЕЙСТВИЕ (глагол) — что сделать руками/головой
2) КОГДА — день недели / «сегодня до 21:00» / «Пн и Чт 07:30»
3) СКОЛЬКО — минут или «до результата X»
4) ГДЕ/ЧЕМ — приложение, документ, человек (из «опоры» пользователя, если есть)
5) РЕЗУЛЬТАТ «ГОТОВО» — что лежит на выходе (файл, сообщение, галочка, число)

Формула строки шага:
«[Когда] — [Действие]: [инструмент/место]. [N минут]. Готово, когда: [измеримый артеfact].»

ЗАПРЕЩЁННЫЕ ФРАЗЫ (без замены на конкретику):
«создавайте ритуалы», «будьте последовательны», «работайте над мотивацией», «используйте сильные стороны», «найдите время», «постарайтесь», «улучшайте», «анализируйте ситуацию» без таблицы/файла/таймера.

ПРОВИДЕЦ (amygdala-dopamine-control):
Сила → в шаг: 60 мин «уродливый прототип» / схема видения под ЭТУ цель.
Плохо: «Придумайте видение цели».
Хорошо (цель «выучить B2»): «Вт 07:00–08:00 — одна страница A4: «Я на B2 когда…» + 3 скрина приложений. Без правок. Готово: файл в заметках.»
Слабость (бросает) → в шаг: жёсткий стоп и передача: «После 1 ч — отправить черновик [имя из опоры] в Telegram, дальше не править 48 ч.»

СТРАТЕГ (hippocampus-dopamine-control):
Сила → таблица рисков / обратное планирование под цель.
Плохо: «Просчитайте риски».
Хорошо: «Ср 19:00 — Google Sheet 3×3: риск | вероятность 1–5 | действие если да. Минимум 5 строк про [цель]. Готово: ссылка сохранена.»
Слабость (паралич) → «Таймер 25 мин на анализ, затем 1 решение в ячейку D1, без доп. колонок.»

ИНЖЕНЕР (hippocampus-serotonin-control):
Сила → чек-лист, DoD 80%, блок 90 мин.
Плохо: «Оптимизируйте процесс».
Хорошо: «Пн–Пт 06:30–08:00 — блок без уведомлений: пункты 1–3 чек-листа [цель]. Стоп на 80%. Готово: 3 галочки в Notion/бумаге.»
Слабость (перфекционизм) → в шаге явно: «Не добавлять пункты сверх списка.»

ХРАНИТЕЛЬ (hippocampus-serotonin-connection):
Сила → ИМЕНОВАННЫЕ ритуалы под цель, не «создавайте ритуалы».
Плохо: «Создавайте ритуалы стабильности».
Хорошо (цель «похудеть на 8 кг»): «Ежедневно 07:15 — «Ритуал опоры»: 5 мин записать в блокнот «Кто мои люди сегодня» + взвешивание + одно и то же приветствие в чат [имя]. Готово: запись с датой.»
«Лента традиций» → перечисли 3 ритуала с временем: утро/день/вечер, каждый привязан к шагу цели.
Слабость (страх перемен) → «Правило одного изменения: на этой неделе только 1 новый элемент — [конкретный].»

КОММУНИКАТОР (amygdala-dopamine-connection):
Сила → публичное обязательство, 1 контакт, голосовое.
Плохо: «Делитесь прогрессом».
Хорошо: «Вт 12:00 — голосовое 60 сек [имя из опоры]: «Сегодня сделал X по [цель]». Готово: сообщение отправлено.»
«Репетиция 2 мин» перед любым публичным шагом к цели — пропиши текст дословно в шаге.

ПОСРЕДНИК (amygdala-serotonin-connection):
Сила → согласование через «говорящее кресло», вопрос «совет или поддержка?».
Плохо: «Попросите поддержку».
Хорошо: «Чт 20:00 — сообщение [имя]: «Мне нужна поддержка, не совет: 10 мин выслушать про [барьер из ответа]». Готово: ответ получен.»
Красная линия → если конфликт, шаг: «Выйти на 10 мин, 10 дыханий, вернуться письменно.»

НОМАД (amygdala-serotonin-control):
Сила → бункер-режим, одна встреча, список не-действий.
Плохо: «Работайте в одиночестве».
Хорошо: «Пн–Сб 09:00–11:00 — «Бункер»: телефон в другой комнате, только [задача цели]. Макс. 1 созвон/день — только 17:00. Готово: черновик N слов/страниц.»

ПИОНЕР (hippocampus-dopamine-connection):
Сила → цель в чат, пятиминутка, ретро.
Плохо: «Объедините команду».
Хорошо: «Пн 08:30 — в чат [имена из опоры]: одна задача дня по [цель]. Ср 13:00 — 5 мин «кто где?». Готово: скрин переписки.»

ПОНЕДЕЛЬНЫЙ ПЛАН (phases[]):
- Каждый элемент phases = ОДНА неделя. Количество недель = реалистичный срок цели (2–12 типично, до 16 максимум).
- Сам выбери число недель (2–16) по реальности цели, не копируй срок пользователя. Не фиксируй 3 недели без причины. Не дублируй одинаковые шаги.
- В каждой неделе 3–6 steps с днями/временем и «готово когда». Часы из hours_per_week распредели по неделям.

СОВЕТЫ (archetype_tips):
Не советы, а мини-задания: та же формула «когда + что + готово», привязка к цели и микропрактике из ЧАСТИ 4 по имени.

ОЩУЩЕНИЕ «МАГИИ»:
Человек читает план и думает: «Это написано про меня и про мою цель — осталось просто делать по списку.»
Каждая фаза: 3–6 шагов на неделю, каждый проходит контракт выше. N фаз = N недель.

ЧАСТЬ 7: ЯСНОСТЬ, ОПОРА И «МАСТЕРСКИЙ ПЛАН»

ПРАВИЛО ОПОРЫ (поле «кто/что поможет» в контексте):
- ЗАПРЕЩЕНО: «выбрать человека из опоры», «написать кому-то из поддержки», «обратиться к опоре» — без имени и канала связи.
- Если пользователь назвал имя — вставь ДОСЛОВНО: «Telegram супруге Анне», «созвон с наставником Игорем».
- Если имени нет — процитируй его ответ про опору и предложи конкретный выбор: «из того, что ты написал (“жена помогает с дисциплиной, друг Петя — с английским”) — выбери одного: жена или Петя; напиши ему/ей: [готовый текст сообщения]».
- В intro одной фразой: «Твоя опора: [кратко как написал пользователь] — в плане используем так: [1–2 примера].»

ПРАВИЛО БАРЬЕРА:
- Барьер из ответа пользователя — не общее слово. В шагах: «когда срабатывает [барьер дословно или близко] — сделай X в Y:00, готово когда Z».

ТОН: МАСТЕРСКИЙ ПЛАН + ПРАЗДНИК АРХЕТИПА
- intro: 8–14 предложений, тёплый уверенный тон — «это твой личный маршрут к цели».
- Названия фаз: короткие, запоминающиеся, про ЭТУ цель (не «Фаза 1», не «Подготовительный этап»).
- Архетип = суперсила: «как [архетип] ты [сильная сторона] — поэтому первые шаги устроены так…»
- Шаги: простые предложения, без канцелярита; можно длиннее, если добавляется ясность (готовый текст сообщения, имя файла, число).
- План должен выглядеть: (1) просто, (2) эффективно, (3) легко в выполнении — потому что всё уже разложено под архетип.

ПРОВЕРКА ПОНИМАНИЯ:
Читатель без дополнительного контекста понимает КАЖДЫЙ шаг: что, когда, чем, сколько, «готово когда» — ноль догадок.
`;

